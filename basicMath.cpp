#include <cmath>
#include <iostream>

int main() {
    std::cout << "9 + 2 = " << 9 + 2 << std::endl;
    std::cout << "7 - 3 = " << 7 - 3 << std::endl;
    std::cout << "10 / 2 = " << 10 / 2 << std::endl;
    std::cout << "-4 / 2 = " << -4 / 2 << std::endl;
    std::cout << "2 * 3 = " << 2 * 3 << std::endl;
    std::cout << "pow(2, 3) = " << pow(2, 3) << std::endl;
    std::cout << "1 / 3 = " << 1.0 / 3.0 << std::endl;
}
